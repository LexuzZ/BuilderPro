package com.example.builderpro.data.model;

public class Alamat {
    public String jalan;
    public String kota;
    public String provinsi;
    public String kode_pos;

    public Alamat(String jalan, String kota, String provinsi, String kode_pos) {
        this.jalan = jalan;
        this.kota = kota;
        this.provinsi = provinsi;
        this.kode_pos = kode_pos;
    }
}

